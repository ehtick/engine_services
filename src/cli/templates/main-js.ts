export function getMainTs(): string {
  const backtick = '`';
  const dollar = '$';
  return [
    'interface ThatOpenContext {',
    '  appId: string;',
    '  projectId: string;',
    '  accessToken: string;',
    '  apiUrl: string;',
    '}',
    '',
    'declare global {',
    '  interface Window {',
    '    __THATOPEN_CONTEXT__?: ThatOpenContext;',
    '  }',
    '}',
    '',
    '// ThatOpen App Entry Point',
    '//',
    '// Access the platform context via window.__THATOPEN_CONTEXT__:',
    '//   - appId:       This app\'s ID',
    '//   - projectId:   The project this app belongs to',
    '//   - accessToken: Auth token for API calls',
    '//   - apiUrl:      Base URL for the ThatOpen API',
    '//',
    '// Your app renders into the #that-open-app element.',
    '',
    'const ctx = window.__THATOPEN_CONTEXT__ || {} as ThatOpenContext;',
    'const app = document.getElementById(\'that-open-app\')!;',
    '',
    'app.innerHTML = ' + backtick,
    '  <div style="padding: 32px; font-family: system-ui, sans-serif; max-width: 600px; margin: 0 auto;">',
    '    <h1 style="color: #6528e0; margin-bottom: 16px;">My ThatOpen App</h1>',
    '    <p style="margin-bottom: 8px;"><strong>App ID:</strong> ' + dollar + '{ctx.appId || \'N/A\'}</p>',
    '    <p style="margin-bottom: 8px;"><strong>Project ID:</strong> ' + dollar + '{ctx.projectId || \'N/A\'}</p>',
    '    <p style="margin-bottom: 8px;"><strong>API URL:</strong> ' + dollar + '{ctx.apiUrl || \'N/A\'}</p>',
    '    <p style="color: #888; margin-top: 24px;">Edit <code>src/main.ts</code> to start building your app.</p>',
    '  </div>',
    backtick + ';',
    '',
  ].join('\n');
}
